void GE_draw_pixel(point_t p) {
    if (p.x >= 128 || p.y >= 64) return;
    canvas_t canvas = sh1106_get_canvas();
    canvas[p.x] |= (1ULL << p.y);
}

void GE_draw_vline(point_t p, uint8_t length) {
    if (p.x >= 128) return;
    canvas_t canvas = sh1106_get_canvas();
    uint8_t y_end = p.y + length;
    if (y_end > 64) y_end = 64;

    for (uint8_t y = p.y; y < y_end; y++) {
        canvas[p.x] |= (1ULL << y);
    }
}

void GE_draw_hline(point_t p, uint8_t length) {
    if (p.y >= 64) return;
    canvas_t canvas = sh1106_get_canvas();
    uint8_t x_end = p.x + length;
    if (x_end > 128) x_end = 128;

    for (uint8_t x = p.x; x < x_end; x++) {
        canvas[x] |= (1ULL << p.y);
    }
}

void GE_draw_fullvline(uint8_t posx) {
    if (posx >= 128) return;
    canvas_t canvas = sh1106_get_canvas();
    canvas[posx] = 0xFFFFFFFFFFFFFFFFULL;
}

void GE_draw_fullhline(uint8_t posy) {
    if (posy >= 64) return;
    canvas_t canvas = sh1106_get_canvas();
    for (uint8_t x = 0; x < 128; x++) {
        canvas[x] |= (1ULL << posy);
    }
}

void GE_draw_rect(point_t start, uint8_t width, uint8_t height) {
    if (width == 0 || height == 0) return;
    point_t end_point = { .x = start.x + width, .y = start.y + height };

    GE_draw_hline(start, width);
    if (height > 1) {
        GE_draw_hline((point_t){start.x, end_point.y - 1}, width);
    }

    GE_draw_vline(start, height);
    if (width > 1) {
        GE_draw_vline((point_t){end_point.x - 1, start.y}, height);
    }
}

void GE_fill_rect(point_t start, uint8_t width, uint8_t height) {
    for (uint8_t i = 0; i < width; i++) {
        GE_draw_vline((point_t){start.x + i, start.y}, height);
    }
}

void GE_putc(point_t pos, char c, uint8_t scale) {
    if (c < ' ' || c > '~') return;

    const unsigned char *glyph = font5x7[c - ' '];

    for (uint8_t i = 0; i < 5; i++) { // 5 columns
        uint8_t col_data = glyph[i];
        for (uint8_t j = 0; j < 8; j++) { // 7 rows in font, but check 8 bits
            if ((col_data >> j) & 0x01) {
                if (scale == 1) {
                    GE_draw_pixel((point_t){pos.x + i, pos.y + j});
                } else {
                    GE_fill_rect((point_t){pos.x + i * scale, pos.y + j * scale}, scale, scale);
                }
            }
        }
    }
}
